/**
 * Core logging, colors, and banner display.
 * Replaces lib/core.sh
 */

import chalk from 'chalk';
import { appendFileSync } from 'node:fs';

// в”Җв”Җ ANSI strip в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
const ANSI_RE = /\x1b(?:[@-Z\\-_]|\[[0-?]*[ -/]*[@-~])/g;
export const stripAnsi = (s) => s.replace(ANSI_RE, '');

// в”Җв”Җ Timestamp в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
export const ts = () => {
  const d = new Date();
  return [d.getHours(), d.getMinutes(), d.getSeconds()]
    .map((n) => String(n).padStart(2, '0'))
    .join(':');
};

// в”Җв”Җ Terminal width в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
export const termWidth = () => process.stdout.columns || 80;

// в”Җв”Җ Raw log (terminal + file, ANSI-stripped in file) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
export function logRaw(msg, logFile) {
  process.stdout.write(msg + '\n');
  if (logFile) {
    try {
      appendFileSync(logFile, stripAnsi(msg) + '\n');
    } catch { /* ignore */ }
  }
}

// в”Җв”Җ Logging functions в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
export const logInfo  = (msg) => console.log(chalk.dim(`  [${ts()}]`) + `  в„№  ${msg}`);
export const logOk    = (msg) => console.log(chalk.dim(`  [${ts()}]`) + chalk.green(`  вң“  ${msg}`));
export const logWarn  = (msg) => console.log(chalk.dim(`  [${ts()}]`) + chalk.yellow(`  вҡ   ${msg}`));
export const logErr   = (msg) => console.error(chalk.dim(`  [${ts()}]`) + chalk.red(`  вң—  ${msg}`));

export function logStep(msg) {
  const w = termWidth();
  const line = 'в”Җ'.repeat(Math.max(w - 4, 20));
  console.log(chalk.dim(`\n  ${line}`));
  if (msg) console.log(chalk.bold(`  ${msg}`));
}

// в”Җв”Җ Agent start/done boxes в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
const BACKEND_COLOR = {
  claude: chalk.blue, copilot: chalk.green,
  codex: chalk.yellow, gemini: chalk.magenta,
};

export function logAgentStart({ role, model, backend, task }) {
  const w = termWidth();
  const color = BACKEND_COLOR[backend] || chalk.white;
  const top = 'в”Ң' + 'в”Җ'.repeat(w - 4) + 'в”җ';
  const bot = 'в”ң' + 'в”Җ'.repeat(w - 4) + 'в”Ө';
  console.log(chalk.dim(`\n  ${top}`));
  console.log(chalk.dim('  в”Ӯ ') + color.bold(`${role}`) + chalk.dim(` В· ${backend}/${model}`));
  if (task) console.log(chalk.dim('  в”Ӯ ') + chalk.dim(task));
  console.log(chalk.dim(`  ${bot}\n`));
}

export function logAgentDone() {
  const w = termWidth();
  const line = 'в””' + 'в”Җ'.repeat(w - 4) + 'в”ҳ';
  console.log(chalk.dim(`\n  ${line}\n`));
}

